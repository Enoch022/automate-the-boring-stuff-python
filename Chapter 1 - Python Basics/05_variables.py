# Variable assignment and overwriting

spam = 40
eggs = 2

print(spam)
print(spam + eggs)
spam = spam + 2
print(spam)

# Overwriting strings
spam = 'Hello'
print(spam)
spam = 'Goodbye'
print(spam)

# Variable naming examples
